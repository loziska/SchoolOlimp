def min_bidons(N):
    # Инициализация массива dp, где dp[i] - минимальное количество бидонов для i литров
    dp = [float('inf')] * (N + 1)
    dp[0] = 0  # Базовый случай: 0 литров - 0 бидонов
    
    for i in range(1, N + 1):
        # Проверяем все возможные варианты добавления бидона
        if i >= 1:
            dp[i] = min(dp[i], dp[i - 1] + 1)
        if i >= 5:
            dp[i] = min(dp[i], dp[i - 5] + 1)
        if i >= 6:
            dp[i] = min(dp[i], dp[i - 6] + 1)
    
    return dp[N]

# Ввод количества литров
N = int(input("Введите количество литров молока: "))
result = min_bidons(N)
print(f"Минимальное количество бидонов: {result}")

# Инициализация массива dp:
# dp[i] хранит минимальное количество бидонов для i литров.
# Изначально все значения dp[i] = ∞, кроме dp[0] = 0.

# Заполнение массива dp:
# Для каждого объема от 1 до N проверяем, можно ли добавить бидон 1, 5 или 6 литров.
# Обновляем dp[i] как минимум из текущего значения и dp[i - volume] + 1.

# Результат:
# dp[N] содержит ответ — минимальное количество бидонов.
